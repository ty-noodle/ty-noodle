import { execSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import process from "node:process";

const rootDir = process.cwd();
const outputPath = resolve(rootDir, "src/types/database.ts");
function fail(message) {
  process.stderr.write(`${message}\n`);
  process.exit(1);
}

function loadEnvFile(filename) {
  const filePath = resolve(rootDir, filename);

  if (!existsSync(filePath)) {
    return;
  }

  const contents = readFileSync(filePath, "utf8");

  for (const line of contents.split(/\r?\n/)) {
    const trimmed = line.trim();

    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }

    const separatorIndex = trimmed.indexOf("=");

    if (separatorIndex <= 0) {
      continue;
    }

    const key = trimmed.slice(0, separatorIndex).trim();
    const rawValue = trimmed.slice(separatorIndex + 1).trim();

    if (!process.env[key]) {
      process.env[key] = rawValue;
    }
  }
}

loadEnvFile(".env");
loadEnvFile(".env.local");

const projectId = process.env.SUPABASE_PROJECT_ID?.trim();
const accessToken = process.env.SUPABASE_ACCESS_TOKEN?.trim();
const schemaList = process.env.SUPABASE_DB_SCHEMAS?.trim() || "public";

if (!projectId) {
  fail("Missing SUPABASE_PROJECT_ID. Add it to .env.local before running npm run gen:types.");
}

if (!accessToken) {
  fail("Missing SUPABASE_ACCESS_TOKEN. Add it to .env.local before running npm run gen:types.");
}

const outputDir = dirname(outputPath);

if (!existsSync(outputDir)) {
  mkdirSync(outputDir, { recursive: true });
}

const env = {
  ...process.env,
  SUPABASE_ACCESS_TOKEN: accessToken,
};

const args = ["--yes", "supabase@latest", "gen", "types", "typescript", "--project-id", projectId];

for (const schema of schemaList
  .split(",")
  .map((value) => value.trim())
  .filter(Boolean)) {
  args.push("--schema", schema);
}

const generated = execSync(`npx ${args.map((value) => `"${value}"`).join(" ")}`, {
  cwd: rootDir,
  encoding: "utf8",
  env,
  stdio: ["pipe", "pipe", "pipe"],
});

const fileContents = `/**
 * Generated by \`npm run gen:types\`
 * Do not edit this file manually.
 */

${generated.trim()}
`;

writeFileSync(outputPath, fileContents, "utf8");

process.stdout.write(`Generated Supabase types at ${outputPath}\n`);
